/**
 * 1.8 (Area and perimeter of a circle)
 * Write a program that displays the area and perimeter of a circle
 * that has a radius of 6.5
 *
 * Created by ag on 30-Sep-20 10:47 PM 
 */
class Ex8 {
  public static void main(String[] args) {
    System.out.println("p = 3.14159");
    System.out.println("radius = 6.5");
    System.out.println("perimeter = 2 * radius * p = " + (2 * 6.5 * 3.14159));
    System.out.println("area = radius * radius * p = " + (6.5 * 6.5 * 3.14159));
  }
}
