package org.mrdrprof.solutions.chapter1_Intro;

/**
 * 1.9 (Area and perimeter of a rectangle)
 * Write a program that displays the area and perimeter of a rectangle with
 * a width of 5.3 and height of 8.6
 *
 * Created by ag on 30-Sep-20 10:50 PM 
 */
class Ex9 {
  public static void main(String[] args) {
    System.out.println("w = 5.3; h = 8.6");
    System.out.println("area = w * h = " + (5.3 * 8.6));
    System.out.println("perimeter = 2 * (w + h) = " + (2 * (5.3 + 8.6)));
  }
}
